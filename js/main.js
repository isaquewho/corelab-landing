let currentLang = 'pt';

function applyLang(lang) {
  currentLang = lang;
  const t = i18n[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.innerHTML = t[key];
  });
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang;
}

/* ------------------------------------------
   SPLASH TAG CYCLE
------------------------------------------ */
const tags = {
  pt: ["Inicializando...", "Carregando módulos...", "Preparando seu treino..."],
  es: ["Inicializando...", "Cargando módulos...", "Preparando tu entrenamiento..."],
  en: ["Initializing...", "Loading modules...", "Preparing your workout..."]
};
let ti = 0;
const splashTag = document.getElementById('splash-tag');
const tagInterval = setInterval(() => {
  ti = (ti + 1) % tags[currentLang].length;
  splashTag.textContent = tags[currentLang][ti];
}, 900);
setTimeout(() => clearInterval(tagInterval), 2800);

/* ------------------------------------------
   NAVBAR SCROLL
------------------------------------------ */
const navbar = document.getElementById('navbar');
window.addEventListener('scroll', () => {
  navbar.classList.toggle('scrolled', window.scrollY > 40);
});

/* ------------------------------------------
   THEME TOGGLE
------------------------------------------ */
const themeToggle = document.getElementById('themeToggle');
let isDark = true;
themeToggle.addEventListener('click', () => {
  isDark = !isDark;
  document.documentElement.setAttribute('data-theme', isDark ? 'dark' : 'light');
  themeToggle.textContent = isDark ? '🌙' : '☀️';
});

/* ------------------------------------------
   LANGUAGE SWITCHER
------------------------------------------ */
document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => {
    document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
    applyLang(btn.dataset.lang);
  });
});

/* ------------------------------------------
   LOGIN MODAL
------------------------------------------ */
const loginBtn = document.getElementById('loginBtn');
const overlay = document.getElementById('login-overlay');
const closeModal = document.getElementById('closeModal');
const submitLogin = document.getElementById('submitLogin');

loginBtn.addEventListener('click', () => {
  if (!loginBtn.classList.contains('logged')) {
    overlay.classList.add('open');
    document.body.style.overflow = 'hidden';
  }
});
closeModal.addEventListener('click', closeLoginModal);
overlay.addEventListener('click', e => {
  if (e.target === overlay) closeLoginModal();
});
document.addEventListener('keydown', e => {
  if (e.key === 'Escape') closeLoginModal();
});
function closeLoginModal() {
  overlay.classList.remove('open');
  document.body.style.overflow = '';
}
submitLogin.addEventListener('click', () => {
  const email = document.getElementById('loginEmail').value;
  if (!email) return;
  loginBtn.classList.add('logged');
  document.getElementById('userAvatar').textContent = email.substring(0, 2).toUpperCase();
  closeLoginModal();
});

/* ------------------------------------------
   EARLY ACCESS FORM
------------------------------------------ */
document.getElementById('earlyForm').addEventListener('submit', e => {
  e.preventDefault();
  const email = document.getElementById('earlyEmail').value;
  if (!email) return;
  document.getElementById('earlyForm').style.display = 'none';
  const msg = document.getElementById('success-msg');
  msg.style.display = 'block';
  msg.textContent = i18n[currentLang].ea_success;
});

/* ------------------------------------------
   SCROLL REVEAL
------------------------------------------ */
const revealEls = document.querySelectorAll('.reveal, .reveal-left, .reveal-right');
const observer = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      observer.unobserve(entry.target);
    }
  });
}, { threshold: 0.12 });
revealEls.forEach(el => observer.observe(el));

